'''
jupyter.py

주피터: 한줄씩 파이썬 코딩하고 바로바로 확인(shift + Enter)

설치 방법(터미널에서)
    pip install jupyter

실행 방법(터미널에서)
    jupyter notebook

명령 모드(활성화하려면 ESC 키 누름)
    shift-Enter: 셀 실행, 아래 선택
    Ctrl-Enter: 셀 실행
    Alt-Enter: 셀 실행, 아래에 삽입
    y : 코딩(마트다운으로 된 셀을 다시 코딩모드로)
    m : 마트다운으로(코딩, 파이썬 명령어가 아니라 문서처럼 쓰고 싶을 때.)
    a : 위에 셀 삽입
    b: 아래에 셀 삽입
    x : 선택한 셀 잘라내기
    c : 선택한 셀 복사
    shift-v: 위에 셀 붙여넣기
    v : 아래에 셀 붙여넣기
    z : 마지막 셀 삭제 취소
    d,d : 선택한 셀 삭제
    shift-M: 아래 셀 병합

    편집 모드 (활성화하려면 Enter 키를 누름)
    shift-Tab : 툴 팁 표시
    Ctrl-] : 들여쓰기
    Ctrl-Shift- : 셀 분할

'''